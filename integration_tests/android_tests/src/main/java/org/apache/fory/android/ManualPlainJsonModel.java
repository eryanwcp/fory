package org.apache.fory.android;

/** Plain JSON model retained by application-authored exact R8 rules. */
public final class ManualPlainJsonModel {
  public int id;
  public String name;

  public ManualPlainJsonModel() {}

  public ManualPlainJsonModel(int id, String name) {
    this.id = id;
    this.name = name;
  }
}
